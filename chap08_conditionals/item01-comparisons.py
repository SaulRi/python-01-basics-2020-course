## Comparison expressions

# equals ==
print( f"5 == 5 : { 5 == 5 }"  )
print( f"5 == 6 : { 5 == 6 } \n"  )

# not equal to !=
# revisar error
print( f"9 = 7 : { 9 != 7 }"  )
print( f"9 = 9 : { 9 != 9 } \n"  )


# less than <
print( f"5 < 6 : { 5 < 6 }"  )
print( f"5 < 5 : { 5 < 5 }"  )
print( f"5 < 4 : { 5 < 4 }  \n"  )


# greater than >
print( f"5 > 4 : { 5 > 4 }"  )
print( f"5 > 5 : { 5 > 5 }"  )
print( f"5 > 6 : { 5 > 6 }  \n"  )


# less or equal than >=
print( f"5 <= 5 : { 5 <= 5 }  "  )
print( f"5 <= 6 : { 5 <= 6 }  "  )
print( f"5 <= 4 : { 5 <= 6 }  \n"  )


# greater or equal than >=
print( f"5 >= 5 : { 5 >= 5 }"  )
print( f"5 >= 4 : { 5 >= 4 }"  )
